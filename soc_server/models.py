    mitre_tactic = db.Column(db.String(120), nullable=True)
    mitre_technique = db.Column(db.String(120), nullable=True)
    timeline = db.Column(db.JSON, nullable=False, default=list)
    matched_alert_ids = db.Column(db.JSON, nullable=False, default=list)
    evidence = db.Column(db.JSON, nullable=False, default=list)
    evidence_hash = db.Column(db.String(64), nullable=False, default="", index=True)
    created_at = db.Column(db.DateTime(timezone=True), nullable=False, default=utc_now)
    resolved_at = db.Column(db.DateTime(timezone=True), nullable=True)
    closed_at = db.Column(db.DateTime(timezone=True), nullable=True)
    updated_at = db.Column(
        db.DateTime(timezone=True),
        nullable=False,
        default=utc_now,
        onupdate=utc_now,
    )

    def __repr__(self) -> str:
        return f"<CorrelatedIncident {self.incident_id} {self.status}>"


