class CorrelatedIncident(db.Model):
    """Incident created by correlating multiple security alerts."""

    __tablename__ = "correlated_incidents"

    id = db.Column(db.Integer, primary_key=True)
    incident_id = db.Column(db.String(24), unique=True, nullable=False, index=True)
    endpoint_id = db.Column(db.String(20), nullable=False, index=True)
    correlation_rule_id = db.Column(db.String(80), nullable=False, index=True)
    severity = db.Column(db.String(30), nullable=False, index=True)
    status = db.Column(db.String(30), nullable=False, default="OPEN", index=True)
    title = db.Column(db.String(200), nullable=False, default="Correlated Incident")
    summary = db.Column(db.Text, nullable=False)
    confidence = db.Column(db.Integer, nullable=False, default=0)
    risk_score = db.Column(db.Integer, nullable=False, default=0)
    assigned_to = db.Column(db.String(120), nullable=True, index=True)
