from datetime import datetime, timezone

from flask_sqlalchemy import SQLAlchemy


# SQLAlchemy is created here and initialized inside the application factory.
db = SQLAlchemy()


def utc_now() -> datetime:
    """Return a timezone-aware UTC timestamp for database defaults."""

    return datetime.now(timezone.utc)


class Endpoint(db.Model):
    """Registered endpoint managed by SOC Sentinel agents."""

    __tablename__ = "endpoints"

    id = db.Column(db.Integer, primary_key=True)
    endpoint_id = db.Column(db.String(20), unique=True, nullable=False, index=True)
    hostname = db.Column(db.String(120), nullable=False)
    username = db.Column(db.String(120), nullable=True)
    operating_system = db.Column(db.String(120), nullable=True)
    os_version = db.Column(db.String(80), nullable=True)
    ip_address = db.Column(db.String(45), nullable=True)
    mac_address = db.Column(db.String(64), nullable=True)
    agent_version = db.Column(db.String(40), nullable=True)
    device_fingerprint = db.Column(db.String(64), nullable=True, index=True)
    identity_status = db.Column(db.String(30), nullable=False, default="Unknown", index=True)
    identity_previous_fingerprint = db.Column(db.String(64), nullable=True)
    identity_observed_fingerprint = db.Column(db.String(64), nullable=True)
    identity_last_changed = db.Column(db.DateTime(timezone=True), nullable=True)
    api_key = db.Column(db.String(128), unique=True, nullable=False, index=True)
    status = db.Column(db.String(30), nullable=False, default="Offline", index=True)
    registered_at = db.Column(db.DateTime(timezone=True), nullable=False, default=utc_now)
    last_seen = db.Column(db.DateTime(timezone=True), nullable=True, index=True)
    created_at = db.Column(db.DateTime(timezone=True), nullable=False, default=utc_now)
    updated_at = db.Column(
        db.DateTime(timezone=True),
        nullable=False,
        default=utc_now,
        onupdate=utc_now,
    )

    def __repr__(self) -> str:
        return f"<Endpoint {self.endpoint_id} {self.hostname}>"


